  <body>
    <p>
      <font color="#ff0000" size="4">YAGNI:&nbsp;(No vas ha necesitarlo = you aren't gonna need it) </font><font size="4">consiste en que no se debe agregar nunca una funcionalidad excepto cuando sean necesaria. </font>
    </p>
    <p>
      <font size="4">La tentacion de escribir codigo que no es necesario, pero que puede serlo en el futuro, tiene las siguientes desventajas: </font>
    </p>
    <ul>
      <li>
        <font size="4">Cuando se desarrollan nuevas funcionalidades se suele sacrificar el tiempo que se destinaria para la funcionalidad basica</font>
      </li>
      <li>
        <font size="4">Las nuevas caracteristicas deben ser depuradas, documentadas y soportadas.</font>
      </li>
      <li>
        <font size="4">Una nueva funcionalidad impone limites a lo que podria ser hecho en el futuro, y podria impedir la implementacion de una caracteristica necesaria mas adelante.</font>
      </li>
      <li>
        <font size="4">Hasta que no esta definido para que se puede necesitar es imposible saber que debe hacer. Puede suceder que cuando se requieran las nuevas funcionalidades, estas no funcionen correctamente</font>
      </li>
      <li>
        <font size="4">Puede derivar en un codigo inflado: el programa se vuelve grande y complicado pero no proporciona mas funcionalidad.</font>
      </li>
      <li>
        <font size="4">Excepto qu haya especificaciones y algun tipo de control de versiones, estas caracteristicas adicionels pueden ser desconocidas por los programadores que podrian hacer uso de ellas.</font>
      </li>
      <li>
        <font size="4">Puede inducir a que se agrueguen nuevas funcionalidades y, como resultado, puede llevar a un efecto &quot;bola de nieve&quot; que puede consumir tiempo y recursos limitados a cambio de ningun beneficio.&nbsp;</font>
      </li>
    </ul>
    <p>
      <font size="4">TDD Se apoya mucho en YAGNI, no se escribe ni una sola linea que no haga estrictamente falta, se lo lleva hasta el extremo.</font>
    </p>
    <p>
      
    </p>
    <p>
      <font color="#ff0000" size="4">YAGNI es el que se opone a interfaz compleata.</font>
    </p>
    <p>
      <font color="#ff0000" size="4">YAGNI significa “You Aren't Gonna Need It”</font><font size="4">&nbsp;(No vas a necesitarlo) Es uno de los principios del </font><font color="#008000" size="4">Extreme Programming</font><font size="4">&nbsp;que indica que un programador no debe agregar funcionalidades extras hasta que no </font>
    </p>
    <p>
      <font size="4">sea necesario. </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Sinonimos: YAGNI</font><font color="#008000" size="4">&nbsp;(you aren't going to need )</font>
    </p>
    <ul>
      <li>
        <font size="4">Generalidad especulativa (Speculative generlaity - Smell code /refactorin)</font>
      </li>
    </ul>
    <p>
      <font color="#ff0000" size="4">Justificacion:</font>
    </p>
    <ul>
      <li>
        <font size="4">Siempre se implementan cosas cuando realmente se necesitan, no cuando se preveen que se necesitan. Por&nbsp;&nbsp;tanto, no se debe agregar funcionalidad hasta que se considere estrictamente necesario.</font>
      </li>
      <li>
        <font size="4">Las caracteristicas innecesarias son inconvenintes por:</font>

        <ul>
          <li>
            <font size="4">El tiempo gastado se toma para la adicion, laprueba o la mejora de la funcionalidad innecesaria. Y posteriormente, las nuevas caracteristicas deben depurarse, documentarse y mantenerse.</font>
          </li>
          <li>
            <font size="4">Conduce a la hinchazon de codigo y el software se hace mas grande y mas complicado. </font>
          </li>
          <li>
            <font size="4">Añadir nuevas caracteristicas pueden surgir otras nuevas caracterisiticas. </font>
          </li>
          <li>
            <font size="4">Si estas nuevas funcionan se implenentan a si esto podria resultar en un efecto bola de nieve.</font>
          </li>
        </ul>
      </li>
    </ul>
    <p>
      <font color="#ff0000" size="4">Violaciones:</font>
    </p>
    <p>
      <font size="4">Inconveniente por: </font>
    </p>
    <ul>
      <li>
        <font size="4">Hasta que la caracteristica es realmente necesaria, es dificil definir completamente lo que debe hacer y probarla. </font>
      </li>
      <li>
        <font size="4">Si la nueva caracteristica no esta bien definida y probada, puede que no funcione correctamente, incluso si eventualmete se necesita.</font>
      </li>
      <li>
        <font size="4">A menos que existan especificaciones y algun tipo de control de revision, la funcion no puede ser conosida por los programadores que podrian hacer uso de ella.</font>
      </li>
      <li>
        <font size="4">Cualquier nueva caracteristica impone restricciones en lo que puede hacer en el futuro, por lo que una caracteristica innecesaria puede interrumpir caracteristicas necesaras que se agrueguen en el futuro.</font>
      </li>
      <li>
        <font size="4">Debe ser utilizado en combinacion con varias otras practicas , tales como refactorizacion continua que implica la continua correccion.</font>
      </li>
    </ul>
    <p>
      <font color="#ff0000" size="4">Ejm:</font><font size="4">&nbsp;La clase IO implementa todos los tipos. </font>
    </p>
    <p>
      <font size="4">Se habla mucho de YAGNI e metodologias agiles </font>
    </p>
    <p>
      <font size="4">TDD se apoya mucho en YAGNI ( se lleva hasta el extremo) por eso se le dice programacion extrema. </font>
    </p>
