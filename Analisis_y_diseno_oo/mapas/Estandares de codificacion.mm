<node TEXT="Atributos de una Interfaz" ID="ID_1966389700" CREATED="1597608556446" MODIFIED="1647321599960" HGAP_QUANTITY="20.75 pt" VSHIFT_QUANTITY="13.5 pt"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
  <head>
    
  </head>
  <body>
    <p>
      <font color="#ff3366" size="4">Interfaz. </font>
    </p>
    <p>
      <font color="#ff3366" size="4">Suficiente: </font><font size="4">Que la interfaz tenga los metodos necesarios para que las clases que se apoyan en mi puedan hacer lo que tengan que hacer (que tengan lo minimo para los demas). </font>
    </p>
    <p>
      <font size="4">* Por suficiente, queremos decir que la clase o módulo captura suficientes caracteristicas de la abstraccion para permitir una interaccion significativa y eficiente. Hacer otra cosa hace que el componente sea inutil. </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Primitiva</font><font color="#ff0099" size="4">:</font><font size="4">&nbsp;Las operaciones que tu brindas en la interfaz, sean un juego de operaciones </font><font color="#ff3333" size="4">suficiente</font><font size="4">&nbsp;para hacer todo lo que quieran los clientes, que combinandolas oportunamente con los clientes puedan hacer lo que quieran pero que no puedan hacer operaciones mas complejas dedicadas a clientes particulares. </font>
    </p>
    <p>
      <font size="4">Operacion primitiva es que es util para todos y combinandola con otros comandos se puede utilizar. </font>
    </p>
    <ul>
      <li>
        <font size="4">Ejm: Pila tiene los elementos </font><font color="#ff3333" size="4">&quot;apilar un elemento&quot; y &quot;desapilar un elemento&quot;</font><font size="4">&nbsp;pero alguien quiere que mi pila apile de pares (de dos en dos) , pero eso ya no son operaciones primitivas y en la clase pila no deberia implementarlas, porque si me pongo a cumplir requerimientos de clientes particulares, esa clase me queda enormemente compleja. </font>
      </li>
    </ul>
    <p>
      <font size="4">En la practica, violaciones de esta caracteristica se detectan&nbsp;muy temprano, tales diferencias&nbsp;&nbsp;se levantan casi cada vez que descubrimos un cliente que debe utilizar esta abstraccion. </font>
    </p>
    <ul>
      <li>
        <font size="4">&nbsp;Una clase conjunto de elementos, Si ofrece elimintar un elemento debera contemplar añadir un elemento </font>
      </li>
    </ul>
    <p>
      <font color="#ff3333" size="4">Completa</font><font size="4">:<b>&nbsp;(el opuesto de YAGNI )</b></font>
    </p>
    <p>
      <font size="4">Si por ejemplo tienes una intefaz que tiene una funcion meterDato() es obvio que tiene que tener la funcion sacarDato()&nbsp;&nbsp;</font><font color="#008000" size="4">&quot;Gredy Buch&quot;</font><font size="4">&nbsp;te recomienda que completes la interfaz(clase) con las operaciones que crees que tiene que tener para estar completa. </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Consejo</font><font size="4">: Si ves claro claro claro que la interfaz va a necesitar ser completada con codigo que no esta especificado en los requerimientos entonces completala, de lo contrario dejala como esta y has YAGNI.</font>
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">En cuanto a principio de interfaz completa, en oposicion esta YAGNI</font>
