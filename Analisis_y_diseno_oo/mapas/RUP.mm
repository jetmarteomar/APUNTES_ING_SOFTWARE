      Ejemplos:
    </p>
    <ul>
      <li>
        Metodología Top-Down (diseño, sin requisitos) Si al momento de partir, no partimos bien, despues de unos meses de trabajo, alguien que esta en una rama profunda se da cuenta de un error en la forma de partir el problema al principio&nbsp;&nbsp;entonces eso puede tener impacto sobre las 7 ramas que ya llevan meses trabajando. La metodologia top-down puede ser util en pequeña escala, para un problema pequeño pero para uno grande puede dar problemas
      </li>
      <li>
        Metodología Bottom-Up (diseño, sin requisitos)Primero&nbsp;&nbsp;programa las partes basicas de un programa luego las que se apoyan en las basicas y vas subiendo de nivel hasta que acabas programando el programa principal, esta tecnologia esta recomendada a gente con muuuucha y buena experiencia pero que ademas tengan experiencia en el dominio. Ejm: si eres un ingeniero de software brillante pero es la primera vez que te encargan un proyecto de optica no es recomentdable que uses esta metodologia porque no tienes la experiencia suficiente como para saber cuales son esas partes que van a necesitar las partes de 2º nivel y luego las de 3º nivel etc.solo la experiencia puede darte esa intuicion.
      </li>
      <li>
        Metodología en Cascada. Es la que mas se uso Lo analizan todo, lo diseñan todo y lo implantan todo&nbsp;y luego lo prueban . Tenia muchos problemas. Si estamos programando hace 8 meses y descubrimos un fallo en la toma de requisitos eso es un caos. Solo usar para proyectos muy pequeños. Generalmente un usuario el primer dia del proyecto no sabe exactamente lo que quiere. Pede ser experto en el dominio pero aveces no sabe decir que es lo que quiere.
      </li>
      <li>
        Metodología en V (doble Cascada) Intentaron arregalr la cascada con la V. Son dos cascadas, Cada una de las varas de la V.&nbsp;&nbsp;Una para produccion y otra para pruebas&nbsp;&nbsp;y se controlaba mejor el caos pero no tuvo mucho exito
      </li>
      <li>
        Metodología Staffing: Es como se le llamo a las metodologias del software libre. Pero no es aplicable porque no tiene horario, pueden haber miles de programadores y no se p uede aplicar a una empresa de desarrollo ya que los programadores miles y miles generalmente son voluntarios. Tenemos n personas con horaro y fechas
      </li>
