    <p>
      Proceco unificado de desarrollo
    </p>
    <p>
      Objetivos: Proceso de desarrollo de software
    </p>
    <p>
      
    </p>
    <p>
      1. Soy cliente con dinero para pagar a tu empresa si tu equipo desarrolla la aplicacion que deseo:
    </p>
    <ul>
      <li>
        ¿Sabrias que y como preguntarme, en que orden y hata que nivel de deatalle?
      </li>
      <li>
        ¿Cual seria el formato adecuado del resultado de las reuniones?
      </li>
      <li>
        ¿Como repartirias las tareas de requisitos entre las personas del equipo de desarrollo? ¿Con cuantas personas empezarias?
      </li>
    </ul>
    <p>
      2. Eres el jefe de proyecto de la aplicacion.
    </p>
    <ul>
      <li>
        ¿Por que parte comenzarias, continuarias y terminarias la produccion de codigo?
      </li>
      <li>
        Que medidas aplicarias para obtener la caidad del software para facilitar la depuracion de errores, la mantenibilidad correctiva, perfectiva o adaptativa? ¿Como aplicarias calidad?
      </li>
      <li>
        ¿Como repartirias las tareas de produccion entre las personas del equipo de desarrollo? ¿Con cuantas empezarias?
      </li>
    </ul>
    <p>
      3. Soy un miembro del equipo de directivos de tu empresa.
    </p>
    <ul>
      <li>
        ¿Sabrias justificarme mes a mes que tiempo se tardara en terminar el proyecto con el personal asignado?
      </li>
      <li>
        ¿Sabrias justificarme el grado de satisfaccion del cliente durante el transcurso del proyecto?
      </li>
      <li>
        ¿Sabrias justificarme cuantas personas añadirias al proyecto para reducir el desarrollo?
      </li>
    </ul>
    <p>
      Responder a estas preguntas es lo que persigue este marco metodologico unificado.
    </p>
