  <body>
    <p>
      Las Disciplinas son &quot;contenedores&quot; usadas para organizar las actividades del proceso que representan una particion de&nbsp;&nbsp;todos los roles, artefactos y actividades en agrupaciones lógicas por áreas de asuntos o especialidades.
    </p>
    <p>
      Existen 10 disciplinas:
    </p>
    <p>
      
    </p>
    <p>
      <b>Disciplinas técnicas:</b>&nbsp;( o de producción ). Son aquellas que conducen a participar en el producto que queremos entregar.
    </p>
    <ul>
      <li>
        Modelado del dominio&nbsp;&nbsp;(Donde tenemos el contexto en que se va a encontrar nuestro sistema informatico, hardware y software. Y va a ayudarnos a establecer el vocabulario de todo el proyecto)
      </li>
      <li>
        Requisitos (Para saber que tengo que programar y no programar lo que me de la gana)
      </li>
      <li>
        Análisis (Con que clases vamos a repartir el codigo para llevar a cabo nuestros requisitos)
      </li>
      <li>
        Diseño (Que tecnologias vamos a aplicar en el código que vamos a programar)
      </li>
      <li>
        Implementación (para escribir el codigo que queremos entregar)
      </li>
      <li>
        Pruebas (para comprobar que lo que estamso entregando funciona como queremos)
      </li>
    </ul>
    <p>
      En una medida o en otra, todas estas diciplinas nos van a ayudar a la implementacion.
    </p>
    <p>
      
    </p>
    <p>
      <b>Disciplinas de soporte</b>: (para que las personas que estan en produccion no se interrumpan y siempre sepan que tienen que hacer)
    </p>
    <ul>
      <li>
        Gestión del Proyecto (En donde hay un jefe de proyecto que constnte mente esta revisando quien tiene que hacer, qué tiene que hacer y cuando lo tiene que hacer)( en caso de que alguien se atrase o adelante, recalcular las tareas asignadas por si chocan con las siguientes tareas) ( constantemente vigilando&nbsp;&nbsp;que todo el mundo tiene tareas)
      </li>
      <li>
        Entorno
      </li>
      <li>
        Configuración y Gestión de Cambios
      </li>
      <li>
        Despliegue
      </li>
    </ul>
    <p>
      Las tres ultimas estan relacionadas con DevOps (developer of pleistore) Dev = developers Ops=operations
    </p>
    <p>
      Debe haber un encargado de las tres diciplinas entorno, configuracion y despliegue para que el equipo pueda trabajar comodamente.
    </p>
    <p>
      Debe tener todos los entornos configurados, con los permisos y librerias necesarias, con versiones y despliegues con pruebas etc.
    </p>
    <p>
      
    </p>
    <p>
