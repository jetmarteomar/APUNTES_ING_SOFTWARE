      Proceso de Desarrollo Software es el conjunto total de actividades necesarias para transformar los requerimier del cliente en un conjunto consistente de artefactos que representan un producto software y, en un momento posterior, para transformar los cambios de estos requerimientos en una nueva versión del producto software.
    </p>
