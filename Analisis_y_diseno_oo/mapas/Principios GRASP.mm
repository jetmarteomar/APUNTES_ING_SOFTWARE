<node TEXT="3 Bajo acoplamiento" FOLDED="true" ID="ID_1461160812" CREATED="1597275715763" MODIFIED="1635286496309" HGAP_QUANTITY="67.25 pt" VSHIFT_QUANTITY="-27 pt"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
  <head>
    
  </head>
  <body>
    <p>
      <b><font color="#ff0000" size="4">Bajo acoplamiento</font><font size="4">&nbsp; </font></b>
    </p>
    <p>
      <b><font color="#ff0000" size="4">Metrica: </font><font color="#000000" size="4">Si una clase esta dependiendo de 3 a 5 clases esta bien , pero si es mas piensalo y trata de mejorar el diseño.</font></b>
    </p>
    <p>
      <font size="4">¿Que es el acoplamiento? </font>
    </p>
    <p>
      <font size="4">El acoplamiento es un grado de dependencia de otros módulos, de unas clases con otras clases. </font>
    </p>
    <ul>
      <li>
        <font size="4">Necesito que exista esa clase para existir yo?. </font>
      </li>
      <li>
        <font size="4">Dependo de esa clase. ? </font>
      </li>
    </ul>
    <p>
      <font size="4">De todas las clases que heredó, mencionó, uso , etc yo dependo entonces estoy acoplado a ellas. </font>
    </p>
    <p>
      <font size="4">Si son clases estáticas, las uso igual estoy acoplado, como a las clases de utilidad. </font>
    </p>
    <p>
      <font size="4">Que unaclase este acoplada a otra no es el problema, el problema es el acoplamiento </font><font color="#000000" size="4"><b>inestable</b></font><font size="4">&nbsp;por interface o implementacion etc. </font>
    </p>
    <p>
      <font size="4"><b>Ejemplo</b>, acoplarse al String de java es irrelevante porque el String de java no va a cambiar. En ese tipo de acoplamiento no hay problema. </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#ff0000" size="4">Problema</font></b><font size="4">. ¿Cómo apoyar el bajo acoplamiento?, ¿el bajo impacto del cambio y el aumento de la reutilización? </font>
    </p>
    <ul>
      <li>
        <font size="4">El acoplamiento es una medida de la fuerza con la que un elemento está conectado a, tiene conocimiento de, o se basa en, otros elementos. </font>
      </li>
      <li>
        <font size="4">Un elemento con bajo (o débil) acoplamiento no es dependiente de muchos otros elementos; &quot;demasiados&quot; es dependiente del contexto, pero se examinará. Estos elementos incluyen clases, subsistemas, sistemas, y así sucesivamente. </font>
      </li>
      <li>
        <font size="4">Estas clases pueden ser indeseables; algunas sufren los siguientes problemas: </font>

        <ul>
          <li>
            <font size="4">Los cambios en las clases relacionadas fuerzan<b>&nbsp;cambios locales. </b></font>
          </li>
          <li>
            <font size="4">Más<b>&nbsp;difícil de entender</b>&nbsp;de manera aislada. </font>
          </li>
          <li>
            <font size="4">Más <b>difícil de reutilizar</b>&nbsp;porque su uso requiere la presencia adicional de las clases de las que es dependiente. </font>
          </li>
        </ul>
      </li>
    </ul>
    <p>
      <b><font color="#ff0000" size="4">Solución</font></b><font size="4">. Asignar responsabilidades de tal manera que el acoplamiento siga siendo bajo. </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#ff0000" size="4">Discusión</font></b><font size="4">: </font>
    </p>
    <ul>
      <li>
        <font size="4"><b>Formas de acoplamiento directo de un Tipo X a un Tipo Y incluyen: </b></font>

        <ul>
          <li>
            <font size="4">Tipo X tiene un atributo que hace referencia a una instancia Tipo Y. </font>
          </li>
          <li>
            <font size="4">Tipo X tiene un método que hace referencia a una instancia de Tipo Y, por cualquier medio. Estos suelen incluir un parámetro o variable local de tipo Tipo Y, o el objeto de retorno de un mensaje es una instancia de Tipo Y. </font>
          </li>
          <li>
            <font size="4">Un objeto Tipo X pide a los servicios de un objeto Tipo Y. </font>
          </li>
          <li>
            <font size="4">Tipo X es una clase derivada directa o indirecta de Tipo Y. </font><font color="#008000" size="4">Existe una tensión entre los conceptos de acoplamiento y herencia:</font><font size="4">&nbsp;las clases débilmente acopladas son deseables; y la herencia, la cual acopla parejas de superclases y sus subclases, nos ayuda a explotar el factor común entre las abstracciones. </font>
          </li>
        </ul>
      </li>
      <li>
        <b><font size="4">Forma&nbsp;de acoplamiento indirecto de un Tipo X a un Tipo Z</font></b>

        <ul>
          <li>
            <font size="4">&nbsp;será cuando el primero envía mensajes a objetos de Tipo Z que devuelvan los objetos de Tipo Y por un acoplamiento directo </font>
          </li>
        </ul>
      </li>
    </ul>
    <p>
      <b><font color="#ff0000" size="4">Discusión</font><font size="4">:</font></b>
    </p>
    <ul>
      <li>
        <font size="4">No hay una medida absoluta de acoplamiento cuando es demasiado alto. Lo que es importante es que un desarrollador puede medir el grado actual de acoplamiento, y evaluar si el aumento dará lugar a problemas. En general, las clases que son inherentemente de naturaleza muy genéricas, y con una alta probabilidad para su reutilización, deberían tener especialmente bajo acoplamiento. </font>
      </li>
      <li>
        <font size="4">El Bajo Acoplamiento alienta la asignación de una responsabilidad de forma que su colocación no aumente el acoplamiento a un nivel tal que conduce a los resultados negativos que el alto acoplamiento puede producir. </font>
      </li>
      <li>
        <font size="4">Un grado moderado de acoplamiento entre clases es normal y necesario para la creación de un sistema orientado a objetos en el que </font>
      </li>
      <li>
        <font size="4">las tareas se cumplen por una colaboración entre los objetos conectados </font>
      </li>
    </ul>
    <p>
      <b><font color="#ff0000" size="4">Discusión</font></b><font size="4">: </font>
    </p>
    <p>
      <font size="4">▫ El Bajo Acoplamiento es un principio a tener en cuenta en todas las decisiones de diseño; es un objetivo fundamental para considerar continuamente. </font>
    </p>
    <p>
      <font size="4">▫ En la práctica, el nivel de acoplamiento por sí solo no puede considerarse en forma aislada de otros principios como el Experto y Alta Cohesión. Sin embargo, es un factor a considerar en la mejora de un diseño. </font>
    </p>
    <p>
      <font size="4">▫ El alto acoplamiento per se no es el problema; es el acoplamiento a elementos que son inestables en alguna dimensión, como su interfaz, su implementación, o su mera presencia. Podemos añadir flexibilidad, encapsular datos e implementaciones y diseñar en general con bajo acoplamiento en muchas áreas del sistema . Pero si ponemos esfuerzos en “posibles futuros&quot; o bajando el acoplamiento en algún punto en el que, de hecho, no hay motivación realista, esto no será un tiempo bien empleado. Los diseñadores tienen que elegir sus batallas en la reducción de acoplamiento y centrarse en los puntos de alta inestabilidad o evolución realista. </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font size="4" color="#ff0000">Contraindicaciones</font></b><font size="4">: </font>
    </p>
    <p>
      <font size="4">▫ Alto Acoplamiento a elementos estables y generalizados rara vez es un problema. Por ejemplo, una aplicación Java J2EE de forma segura se puede acoplar a las bibliotecas de Java (java.util, y así sucesivamente), porque son estables y generalizadas </font>
    </p>
    <p>
      <font size="4" color="#ff0000"> <b>Beneficios</b></font><font size="4">: </font>
    </p>
    <p>
      <font size="4">▫ No se ve afectado por los cambios en otros componentes ▫ Fácil de entender de manera aislada </font>
    </p>
    <p>
      <font size="4">▫ Conveniente para reutilizar </font>
    </p>
    <p>
      
    </p>
    <p>
      <font size="4" color="#008000">Lo verdaderamente peligroso no es el acoplamiento en sí, sino estar acoplado a un montón de clases inestables que no paran de cambiar y me hacen inestable a mi.</font>
    </p>
