<node TEXT="2 Alta cohesion" FOLDED="true" ID="ID_325113565" CREATED="1597275710987" MODIFIED="1642030161561" HGAP_QUANTITY="67.25 pt" VSHIFT_QUANTITY="-5.25 pt"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
  <head>
    
  </head>
  <body>
    <p>
      <b><font color="#ff0000" size="4">Alta Cohesion:</font><font size="4">&nbsp;</font></b><font size="4">las clases de un codigo deben estar estrechamente relacionadas con la responsabilidad que tiene esa clase. </font>
    </p>
    <p>
      <font color="rgb(32, 33, 34)" face="sans-serif" size="4">Nos dice que la información que almacena una&nbsp;</font><a href="https://es.wikipedia.org/wiki/Clase_(inform%C3%A1tica)" title="Clase (informática)" style="text-decoration: none; color: rgb(6, 69, 173); background-color: rgb(255, 255, 255); background-image: null; background-repeat: repeat; background-attachment: scroll; background-position: null; font-family: sans-serif; font-size: 14px; font-style: normal; font-weight: 400; letter-spacing: normal; text-align: start; text-indent: 0px; text-transform: none; white-space: normal; word-spacing: 0px"><font color="rgb(6, 69, 173)" face="sans-serif" size="4">clase</font></a><font color="rgb(32, 33, 34)" face="sans-serif" size="4">&nbsp;debe ser coherente y debe estar (en la medida de lo posible) relacionada con la clase.</font>
    </p>
    <p>
      <b><font color="#ff0000" size="4">Problema</font></b><font size="4">: </font><font color="#008000" size="4">¿Cómo mantener la complejidad manejable?</font><font size="4">, las clases tienen que ser </font><font color="#008000" size="4">cohesivas</font><font size="4">, todas las lineas del codigo de una clase tienen que estar estrechamente relacionadas con la responsabilidad que tiene esa clase. </font>
    </p>
    <p>
      <font size="4">● La cohesión, o más específicamente , la cohesión funcional, es una media de cómo de fuerte están relacionadas y enfocadas las responsabilidades que son de un elemento. </font>
    </p>
    <p>
      <font size="4">● Un elemento con responsabilidades muy relacionadas y que no hace una enorme cantidad de trabajo, tiene una alta cohesión. Estos elementos incluyen clases, subsistemas, y así sucesivamente. </font>
    </p>
    <p>
      <font size="4">● Una clase con baja cohesión, hace muchas cosas no relacionadas, o hace demasiado trabajo. Clases de baja cohesión a menudo representan un</font><font color="#008000" size="4">&nbsp;“grano muy grande”</font><font size="4">&nbsp;de abstracción, o han asumido responsabilidades que deberían ser delegadas a otros objetos. </font>
    </p>
    <p>
      <font size="4">● Estas clases son indeseables, adolecen de los siguientes problemas. </font>
    </p>
    <ul>
      <li>
        <font size="4">Difícil de comprender </font>
      </li>
      <li>
        <font size="4">Difícil de utilizar </font>
      </li>
      <li>
        <font size="4">Difícil de manejar </font>
      </li>
      <li>
        <font size="4">Difícil de mantener </font>
      </li>
      <li>
        <font size="4">Delicada estructura (constantemente afectada por cambios) </font>
      </li>
    </ul>
    <p>
      
    </p>
    <p>
      <font size="4">Les suelen llamar clases de utilidad (es un cajón de sastre donde meter todo lo que no me cabe) </font>
    </p>
    <p>
      <font color="#008000" size="4">ejem</font><font size="4">: la clase System de java que hace de todo y es un error en el diseño. </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#ff0000" size="4">Solución</font></b><font size="4">: Asignar una responsabilidad para que la cohesión siga siendo alta. </font>
    </p>
    <p>
      <font color="#008000" size="4">Ejm</font><font size="4">: Si calculo en una clase el mes, es muy probable que necesite el año y el dia tambien, eso quiere decir que estos datos están estrechamente relacionados y la cohesión entre ellos es alta. </font>
    </p>
    <p>
      <b><font color="#ff0000" size="4">Discusión</font></b><font size="4">: </font>
    </p>
    <p>
      <font size="4">● Alta cohesión es un principio a tener en cuenta en todas las decisiones de diseño, es un objetivo fundamental para considerar continuamente. Es un principio valorativo que un diseñador aplica al evaluar todas las decisiones de diseño. </font>
    </p>
    <p>
      <font size="4">● El patrón de alta cohesión como muchas cosas en la tecnología de objetos tiene una analogía del mundo real. Es una observación común que si una persona toma demasiadas responsabilidades no relacionadas, en especial las que adecuadamente se deben delegar a otros, entonces la persona no es eficaz. </font>
    </p>
    <p>
      <b><font color="#ff0000" size="4">Discusión</font></b><font size="4">: </font>
    </p>
    <p>
      <font size="4">● Escenarios que ilustran diversos grados de cohesión funcional. ○ Muy baja cohesión: Una clase es la única responsable de muchas cosas en muy diferentes áreas funcionales. </font>
    </p>
    <p>
      <font size="4">○ Baja cohesión: Una clase tiene una responsabilidad exclusiva de una tarea de una tarea compleja en un área funcional. </font>
    </p>
    <p>
      <font size="4">○ Cohesión moderada: Una clase tiene responsabilidades ligeras y únicas en unas pocas áreas diferentes que están lógicamente relacionadas con el concepto de la clase, pero no entre sí. </font>
    </p>
    <p>
      <font size="4">○ Alta cohesión: Una clase tiene responsabilidades moderadas en un área funcional y colabora con otras clases para cumplir con las tareas. Como regla general, una clase con alta cohesión tiene un número relativamente pequeño de métodos, con una funcionalidad muy relacionadas, y no hace demasiado trabajo. Colabora con otros objetos para compartir el esfuerzo si la tarea es grande. </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#ff0000" size="4">Contraindicaciones</font></b><font size="4">. Hay unos pocos casos en los que se justifica la aceptación de una cohesión menor </font>
    </p>
    <p>
      <font size="4">● Uno de los casos es la agrupación de responsabilidades o código en una clase o componente para simplificar el mantenimiento de una persona aunque se advierte </font>
    </p>
    <p>
      <font size="4">que tal agrupación también puede hacer el mantenimiento peor: Clases de Utilidad </font>
    </p>
    <p>
      <font size="4">● Otro caso para los componentes con menor cohesión es con objetos de servidores distribuidos. Debido a las implicaciones generales y de rendimiento asociados con </font>
    </p>
    <p>
      <font size="4">objetos remotos y la comunicación a distancia, a veces es deseable para crear menos y más grandes objetos servidores menos cohesivos que proporcionan una </font>
    </p>
    <p>
      <font size="4">interfaz para muchas operaciones. </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#ff0000" size="4">Beneficios</font></b><font size="4">: </font>
    </p>
    <p>
      <font size="4">● Se aumenta la claridad y facilidad de comprensión del diseño. </font>
    </p>
    <p>
      <font size="4">● Se simplifican mantenimiento y mejoras . </font>
    </p>
    <p>
      <font size="4">● Alta Cohesión es a menudo compatible con Bajo Acoplamiento </font>
    </p>
    <p>
      <font size="4">● El grano fino de la funcionalidad altamente relacionada soporta una mayor reutilización porque una clase cohesiva se puede utilizar para un propósito muy específico</font>
    </p>
  </body>
