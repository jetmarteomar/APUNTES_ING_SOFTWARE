    <p>
      <font color="rgb(32, 33, 34)" face="sans-serif" size="4">El GRASP de experto en información es el principio básico de asignación de responsabilidades. </font>
    </p>
    <p>
      <font color="rgb(32, 33, 34)" face="sans-serif" size="4">Nos indica, por ejemplo, que la responsabilidad de la creación de un objeto o la implementación de un método, debe recaer sobre la clase que conoce toda la información necesaria para crearlo (Quien tenga la informacion). De este modo obtendremos un diseño con mayor cohesión y así la información se mantiene encapsulada (disminución del acoplamiento). </font>
    </p>
    <p>
      <font color="rgb(32, 33, 34)" face="sans-serif" size="4">Si estoy haciendo muchos </font><font color="#ff0000" face="sans-serif" size="4"><b>get()</b></font><b><font color="rgb(32, 33, 34)" face="sans-serif" size="4">&nbsp;</font></b><font color="rgb(32, 33, 34)" face="sans-serif" size="4">es muy mala señal, porque estoy robando la informacion. Lo que tenia que hacer esta informacion lo esta haciendo otra en otro lado.</font>
    </p>
