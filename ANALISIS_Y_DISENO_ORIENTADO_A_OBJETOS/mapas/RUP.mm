<node TEXT="Artefacto" ID="ID_1648702608" CREATED="1647445887111" MODIFIED="1647446455182"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
  <head>
    
  </head>
  <body>
    <p>
      Un <b>artefacto</b>&nbsp;es una pieza de informacion que es producida, modificada o usada por un proceso.
    </p>
    <ul>
      <li>
        Ejemplos: código fuente, especificación de un caso de usco, arquitectura del software, ...
      </li>
      <li>
        Típicamente , los artefactos no son documentos abiertos, son formales: código, UML, ..
      </li>
      <li>
        Los artefactos son productos tangibles de un proyecto: los elementos que el proyecto produce o usa mientras se trabaja hacia el producto
      </li>
      <li>
        Los artefactos son usados como entrada por los diferentes roles (personas) para realizar una actividad y son el resultado o salida de tales actividades
      </li>
      <li>
        Los artefactos son muy posiblemente sujetos al control de versiones y gestión de la configuración
      </li>
    </ul>
  </body>
</html>
