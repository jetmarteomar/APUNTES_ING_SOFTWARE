<node TEXT="Rol" ID="ID_488387817" CREATED="1647445891910" MODIFIED="1647446672992"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
  <head>
    
  </head>
  <body>
    <p>
      Un <b>rol</b>&nbsp;define el comportamiento y responsabilidad de individuo o un grupo de individuos juntos como un equipo.
    </p>
    <ul>
      <li>
        &nbsp;Ejemplos: analista de sistemas, ingeniero de componentes, &nbsp;integrador de sistemas, ...
      </li>
      <li>
        &nbsp;El comportamiento es expresado en términos de actividades &nbsp;que el rol realiza y cada rol es asociado con un conjunto &nbsp;cohesivo de actividades (cada rol es asociado con un conjunto &nbsp;de habilidades para realizar las actividades del rol)
      </li>
      <li>
        La responsabilidad del cada rol son generalmente expresadas &nbsp;en relación a ciertos artefactos que el rol crea, modifica o &nbsp;controla
      </li>
      <li>
        Los roles no son individuos sino que son títulos de trabajo: tú &nbsp;puedes jugar el rol de Gestor de Proyecto por una hora, entonces jugar el rol de Arquitecto por el resto de la mañana e intercambiar los&nbsp;roles de Programador y Probador durante la tarde.
      </li>
    </ul>
  </body>
</html>
