<node TEXT="Actividad" ID="ID_1805327279" CREATED="1647445894600" MODIFIED="1647446801081"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
<html>
  <head>
    
  </head>
  <body>
    <p>
      Una actividad es una unidad de trabajo que puede ser solicitado o realizado por un rol individual y que produce un resultado significativo en el contexto del proyecto
    </p>
    <ul>
      <li>
        Ejemplos: especificar un caso de uso, implementar una clase, ejecutar pruebas de rendimiento, planificar una iteración
      </li>
      <li>
        La actividad tiene un propósito claro, generalmente expresado en términos de crear o modificar artefactos, tales como un modelo, una clase o un plan
      </li>
      <li>
        Cada actividad es asignada a un rol específico
      </li>
      <li>
        Generalmente involucra a una persona del rol asociado y afecta a un solo o un pequeño número de artefactos
      </li>
      <li>
        La granularidad de una actividad es generalmente entre unas horas o unos pocos días
      </li>
    </ul>
  </body>
