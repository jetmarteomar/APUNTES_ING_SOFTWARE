    <p>
      <font size="4">El <b>comportamiento de una clase</b>&nbsp;es el conocimiento que un objeto tiene que tener y las acciones que puede realizar. </font>
    </p>
    <p>
      <font size="4">Las responsabilidades son todos los servicios que presta a todos los contratos&nbsp;&nbsp;que apoya. (contrato es lo que me pide otra clase) </font>
    </p>
    <p>
      
    </p>
    <p>
      <b><font size="4">Estrategia: </font></b><font size="4">Mientras que estos enfoques clásicos se centran en cosas tangibles en el dominio del problema, otra escuela de pensamiento en el análisis orientado a objetos se centra en el </font><font size="4" color="#ff0000">comportamiento dinámico</font><font size="4">&nbsp;como la fuente primaria de clases y objetos. </font>
    </p>
    <ul>
      <li>
        <font size="4">En esta estrategia hacen hincapié en las </font><font size="4" color="#ff0000">responsabilidades</font><font size="4">, que denotan &quot;el conocimiento de un objeto mantiene y las acciones que un objeto puede realizar. </font>
      </li>
      <li>
        <font size="4">Las responsabilidades tienen el propósito de transmitir un sentido de la finalidad de un objeto y su lugar en el sistema. </font>
      </li>
      <li>
        <font size="4">Las responsabilidades de un objeto son todos los servicios que presta a todos los contratos que apoya“ [Wirfs-Brock, Wilkerson y Wiener] </font>
      </li>
    </ul>
    <p>
      <b><font size="4" color="#ff0000">Responsabilidades</font><font size="4">:</font></b><font size="4">&nbsp;las obligaciones de un objeto en términos de su comportamiento. </font>
    </p>
    <p>
      <font size="4">▫ Existen dos tipos básicos: </font>
    </p>
    <ul>
      <li>
        <font size="4">La responsabilidad&nbsp;&nbsp;de un objeto es algo en sí mismo, como la creación de un objeto o hacer un cálculo, iniciar acciones en otros objetos, el control y la coordinación de actividades en otros objetos </font>
      </li>
      <li>
        <font size="4">La responsabilidad de conocer de un objeto es: sobre unos datos privados encapsulados, sobre objetos relacionados, y sobre las cosas que pueden obtener o calcular </font>
      </li>
      <li>
        <font size="4">Se implementan utilizando métodos que, o bien actúan solos o colaboran con otros métodos y objetos. Una responsabilidad no es lo mismo que un método y se ve influida por la granularidad de la responsabilidad. </font>
      </li>
      <li>
        <font size="4">El acceso a las bases de datos relacionales puede implicar decenas de clases y cientos de métodos, empaquetados en un subsistema. </font>
      </li>
    </ul>
    <p>
      <font size="4">• Por el contrario, la responsabilidad de &quot;crear una venta&quot; puede implicar sólo uno o unos métodos </font>
    </p>
    <p>
      <font size="4" color="#ff0000">Estrategia:</font><font size="4">&nbsp;</font>
    </p>
    <p>
      <font size="4">▫ A medida que los miembros del equipo caminan a través del escenario, pueden asignar nuevas responsabilidades a una clase existente, agrupar ciertas responsabilidades para formar una nueva clase, o más comúnmente, dividen las responsabilidades de una clase en más de grano fino y distribuyen estas responsabilidades a clases diferentes. </font>
    </p>
    <p>
      <font size="4">▫ Se crea una tarjeta para cada clase identificada como relevantes para el escenario. Tarjetas CRC (class-responasability-colaborations) han demostrado ser una herramienta de desarrollo útil que facilita el intercambio de ideas y mejora la comunicación entre los desarrolladores. Una tarjeta CRC no es más que una tarjeta de 3x5 pulgadas (7x12,5 cms), en la que el analista escribe en lápiz el nombre de una clase (en la parte superior de la tarjeta), sus responsabilidades (en un medio de la tarjeta), y sus colaboradores ( en la otra mitad de la tarjeta). Esto está superado por las herramientas CASE con UML </font>
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">Antes se hacian en diseño, las clases sin comportamiento (sin metodos), pondera la responsabilidad, deja de mir las clases solo por los metodos y fijate tambien en el comportamiento que estas tienen. Un reparto de responsabilidades entre los metodos. </font>
    </p>
    <p>
      <font size="4" color="#ff0000">Responsabilidad de una clase:</font><font size="4" color="#000000">&nbsp;El conocimiento que un objeto tiene que mantener y las acciones que puede realizar. </font>
    </p>
    <p>
      <font size="4" color="#ff0000">Responsabilidades de un objeto</font><font size="4" color="#000000">. Todos los servicios que presta a todos los contratos que apoya. </font>
    </p>
    <p>
      <font size="4" color="#008000">Contrato: Se refiere a que otra clase me pide cosas a travez de mi interfaz. </font>
    </p>
    <p>
      <font size="4" color="#000000">Eintonces hay que prestar atencion a las clases no solo por los atributos sino que tambien por los metodos. </font>
    </p>
    <p>
      <font size="4">Algunos cuando plantea un problema solo muestra las clases, algunos muestran clases con atributos pero muy pocos diseñan las clases con metodos al plantear un problema. hay que poner esfuerzo en llegar mas profundo en el diseño. Cuando hago un planteamiento tengo que cubrir ambas partes, datos y operaciones. </font>
