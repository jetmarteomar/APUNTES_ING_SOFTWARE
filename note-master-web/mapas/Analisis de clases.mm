      <li>
        <u><font size="4">Cosas, objetos físicos o grupos de objetos que son tangibles:</font></u><font size="4">&nbsp;coches, datos de telemetría, sensores de presión </font>
      </li>
      <li>
        <u><font size="4">Conceptos, principios o ideas no tangibles </font></u><font size="4">por se utilizados para organizar o realizar un seguimiento de las actividades comerciales y/o comunicaciones: préstamo, reunión, intersección </font>
      </li>
