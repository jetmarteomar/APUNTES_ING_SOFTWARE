    <p>
      <font color="#ff0033" size="4"><b>Abbott</b></font><font size="4">&nbsp;sugiere escribir una descripción del problema (o una parte de un problema) y luego subrayar los</font><font color="#ff0033" size="4">&nbsp;sustantivos y verbos. </font><font size="4">Los nombres representan objetos candidatos, y los verbos representan operaciones candidatos en ellos. El enfoque de Abbott es útil porque es simple y porque obliga a los desarrolladores a trabajar en el vocabulario del espacio del problema. </font>
    </p>
    <p>
      <b><font size="4">Estrategia:</font></b><font size="4">&nbsp; </font>
    </p>
    <p>
      <font size="4">Sustantivos = Clases </font>
    </p>
    <p>
      <font size="4">Verbos = Metodos </font>
    </p>
    <p>
      <b><font color="#ff0000" size="4">Inconveniente</font><font size="4">:</font></b>
    </p>
    <p>
      <font size="4">▫ Sin embargo, de ninguna manera es un enfoque riguroso y sin duda no escala bien para nada más allá de problemas bastante triviales. </font>
    </p>
    <p>
      <font size="4">El lenguaje humano es un vehículo de expresión</font><font size="4" color="#008000">&nbsp;tremendamente impreciso</font><font size="4">, por lo que la calidad de la lista resultante de los objetos y las operaciones depende de la habilidad de la escritura de su autor. </font>
    </p>
    <p>
      <font size="4">▫ Por otra parte, cualquier sustantivo puede ser verbo, y cualquier verbo puede ser sustantivo (</font><font color="#008000" size="4">cosificación</font><font size="4">). </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Ej.</font><font size="4">: gestionar vs gestión; oxigeno vs oxigenar.</font>
    </p>
    <p>
      <font size="4">Convertir una accion en una cosa (abeces es conveniente cosificar algunos vervos y volverlos clases). Esta estrategia es util coando no sabes por donde empezar.</font>
    </p>
    <p>
      <font size="4">Una estrategia es cosificar para encontrar esas clases (clasificar -&gt; clasificador) y las funciones sustantivos. </font>
    </p>
    <p>
      <font size="4">Hay que cuidar de no cosificar todo , solo bajo unas ciertas cirscurstancias que convengan.</font>
    </p>
