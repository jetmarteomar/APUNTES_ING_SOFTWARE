        <u><font size="4">Cosas que pasan, </font></u><font size="4">por lo general de otra cosa en una fecha determinada, eventos: aterrizaje, interrumpir, solicitud </font>
      </li>
      <li>
        <u><font size="4">Gente, seres humanos </font></u><font size="4">que llevan a cabo alguna función, usuarios que juegan diferentes roles en la interacción con la aplicación: madre, profesor, político </font>
      </li>
    </ul>
    <p>
      <b><font color="#ff0000" size="4">Estrategia fuentes de clases y objetos </font><font size="4">:</font></b>
    </p>
    <ul>
      <li>
        <u><font size="4">Organizaciones, </font></u><font size="4">colecciones formalmente organizadas de personas y recursos que tienen una misión definida, cuya existencia es en gran medida independiente de los individuos </font>
