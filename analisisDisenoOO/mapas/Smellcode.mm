<node TEXT="Herencia rechazada" ID="ID_47864265" CREATED="1597904852595" MODIFIED="1641866366423" HGAP_QUANTITY="22.25 pt" VSHIFT_QUANTITY="-11.25 pt"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
