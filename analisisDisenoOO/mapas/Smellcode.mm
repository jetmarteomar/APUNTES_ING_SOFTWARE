  <body>
    <p>
      <font color="#ff0000" size="4">Herencia rechazada: </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Codigo sucio por herencia rechazada </font>
    </p>
    <p>
      <font color="#000000" size="4">La clase padre define metodo que la clase hija deja como vacio y no lo usa </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Solucion: <a href="Patronesdediseno.mm">Patron Metodo Plantilla</a>&nbsp;</font>
    </p>
    <p>
      
    </p>
    <p>
      <font color="#ff0000" size="4">SIGNOS Y SINTOMAS</font>
    </p>
    <p>
      <font size="4">Si una subclases usa solo algunos de los metodos y propiedades inerentes de sus padres, la jerarquia (herencia) esta fuera del lugar . Los innecesarios metodos pueden simplemente ir a no usados o ser redefinidos y dar lugar a exepciones </font>
    </p>
    <p>
      <font size="4">Herencia rechazada: una clase que sobreescribe un método de una clase base de tal manera que el contrato de la clase base no es honrado por la clase hija </font>
    </p>
    <p>
      <font color="#ff0000" size="4">RAZON DE PROBLEMA</font>
    </p>
    <p>
      <font size="4">Alguien estaba motivado a crear herencia entre clases solo por el deseo de reutilizar el código en una superclase.&nbsp;&nbsp;Pero la superclase y la subclase son completamente diferentes. </font>
    </p>
    <p>
      <font color="#ff0000" size="4">TRATAMIENTO&nbsp;</font>
    </p>
    <ul>
      <li>
        <font size="4">Si la herencia no tiene sentido y la subclase realmente no tiene nada en común con la superclase, elimine la herencia a favor de Reemplazar herencia con delegación.&nbsp;</font>
      </li>
      <li>
        <font size="4">Si la herencia es apropiada, elimine los campos y métodos innecesarios en la subclase.&nbsp;&nbsp;Extraiga todos los campos y métodos necesarios para la subclase de la clase principal, colóquelos en una nueva subclase y configure ambas clases para heredar de ella (Extraer superclase).</font>
      </li>
    </ul>
    <p>
      <font color="#ff0000" size="4">CONSECUENCIA</font>
    </p>
    <p>
      <font size="4">Mejora la claridad y la organización del código.&nbsp; </font>
    </p>
    <p>
      <font size="4">Ya no tendrá que preguntarse por qué la clase Perro se hereda de la clase Silla (aunque ambas tienen 4 patas). </font>
    </p>
    <p>
      <font size="4">Algo de la clase padre tien que tener sentido en la clase hija. Que un metodo no se use o se deje vacio. y searregla con el metodo plantilla pero bien codificado </font>
    </p>
    <p>
      <font color="#0000c0" size="4">Una clase que sobreescribe un método de una clase base de tal manera que el contrato de la clase base no es honrado por la clase&nbsp;&nbsp;derivada. Ver principio de sustitución de Liskov.</font>
    </p>
