  <body>
    <p>
      SEÑALES Y SINTOMAS
    </p>
    <p>
      <font size="4">Metodos largos. </font>
    </p>
    <p>
      <font size="4">Principio &quot;Mantelno sencillo, estupido&quot; </font>
    </p>
    <p>
      <font size="4">Por encima de todo el codigo debe ser sencillo, y los metodos largos no son admisibles. Si no esta justificada la necesidad entonces fuera. </font>
    </p>
    <p>
      <font size="4">Los metodos deben tener como maximo de 10 a 15 lineas maximo 20 lineas. Porque si no, no se puede debuguear. </font>
    </p>
    <p>
      
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">Un metodo grande tiene muchas lineas de codigo. Generalmente, cualquier metodo largo como de diez lineas te haria preguntarte cuestiones. </font>
    </p>
    <p>
      <font size="4">RASONES PARA EL PROBLEMA </font>
    </p>
    <p>
      <font size="4">Al igual que el Hotel California, siempre se agrega algo a un método, pero nunca se quita nada.&nbsp; </font>
    </p>
    <p>
      <font size="4">Dado que es más fácil escribir código que leerlo, este &quot;olor&quot; pasa desapercibido hasta que el método se convierte en una bestia fea y enorme.&nbsp;&nbsp; </font>
    </p>
    <p>
      <font size="4">Mentalmente, a menudo es más difícil crear un método nuevo que agregarlo a uno existente: &quot;Pero son solo dos líneas, no sirve de nada crear un método completo solo para eso ...&quot; Lo que significa que se agrega otra línea y luego todavía&nbsp;&nbsp;otro, dando a luz a una maraña de código espagueti. </font>
    </p>
    <p>
      <font size="4">TRATAMIENTO </font>
    </p>
    <p>
      <font size="4">&nbsp;Como regla general, si siente la necesidad de comentar algo dentro de un método, debe tomar este código y ponerlo en un nuevo método.&nbsp;&nbsp; </font>
    </p>
    <p>
      <font size="4">Incluso una sola línea puede y debe dividirse en un método separado, si requiere explicaciones.&nbsp;&nbsp;Y si el método tiene un nombre descriptivo, nadie necesitará mirar el código para ver qué hace. </font>
    </p>
    <p>
      <font size="4">*Para reducir la longitud del cuerpo de un método, utilice Extraer método.&nbsp;&nbsp; </font>
    </p>
    <p>
      <font size="4">*Si las variables y los parámetros locales interfieren con la extracción de un método, utilice Reemplazar temperatura con consulta, Introducir objeto de parámetro o Conservar todo el objeto.&nbsp;&nbsp; </font>
    </p>
    <p>
      <font size="4">*Si ninguna de las recetas anteriores ayuda, intente mover todo el método a un objeto separado a través de Reemplazar método con objeto de método.&nbsp;&nbsp; </font>
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">BENEFICIO </font>
    </p>
    <p>
      <font size="4">Entre todos los tipos de código orientado a objetos, las clases con métodos cortos viven más tiempo.&nbsp; </font>
    </p>
    <p>
      <font size="4">Cuanto más largo sea un método o función, más difícil será comprenderlo y mantenerlo.&nbsp; </font>
    </p>
    <p>
      <font size="4">Además, los métodos largos ofrecen el escondite perfecto para el código duplicado no deseado.. </font>
    </p>
    <p>
      <font size="4">RENDIMIENTO </font>
    </p>
    <p>
      <font size="4">¿Un aumento en el número de métodos perjudica el rendimiento, como afirman muchas personas?&nbsp;&nbsp;En casi todos los casos, el impacto es tan insignificante que ni siquiera vale la pena preocuparse.&nbsp; </font>
    </p>
    <p>
      <font size="4">Además, ahora que tiene un código claro y comprensible, es más probable que encuentre métodos realmente efectivos para reestructurar el código y obtener ganancias reales de rendimiento si alguna vez surge la necesidad. </font>
    </p>
    <p>
      
    </p>
