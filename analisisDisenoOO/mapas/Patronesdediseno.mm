<html>
  <head>
    
  </head>
  <body>
    <p>
      <font color="#ff0000" size="4">Patron de diseño Metodo Plantilla (Template Method) </font>
    </p>
    <p>
      <font color="#000000" size="4">El patrón Método Plantilla define el esqueleto de un algoritmo dentro de un método y delega algunos pasos a las subclases. Le permite a las subclases redefinir ciertos pasos del algoritmo sin cambiar su estructura.</font>
    </p>
    <p>
      <font size="4">El objetivo principal de este patrón es definir una plantilla o estructura para algún proceso/algoritmo dentro de nuestra aplicación. </font>
    </p>
    <p>
      <font size="4">Una de las ventajas que nos ofrece este patrón es la de poder crear “anzuelos” o “ganchos” (hooks). Estos anzuelos se utilizan para brindar información adicional y poder acceder a partes específicas dentro del algoritmo. </font>
    </p>
    <p>
      <font size="4">Se crea en la clase padre metodos plantilla que seran implementadas en las clases hijas, </font>
    </p>
    <p>
      <font size="4">El codigo comun se deja como esta en la clase padre y el codigo diferente se lo declara como abstracto </font>
    </p>
