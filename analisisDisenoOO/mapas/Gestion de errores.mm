      <font size="4">&nbsp;Programacion defenciva:&nbsp;&nbsp;si se saca if algo then mustra un error en la pantalla pero eso es un Horror. Si la aplicacioon va a movil ya no podra mostrar en pantalla nada. La programacion defenciva es una cosa horrorosa. </font>
    </p>
    <p>
      <font size="4">La alternativa es el manejo de exepciones. lo cual es horroroso tambien. Las exepciones son para el tratamiento de errores excepcionales que se salen de los limites del software. ho se conecta a la base de datos. ho existe el fichero de texto, etc. </font>
    </p>
    <p>
      <font size="4">Si yo programo una biblioteca y alguien me llama herroneamente yo levanto exepciones porque estoy cumunicando afuera del limite delsoftware biblioteca. </font>
