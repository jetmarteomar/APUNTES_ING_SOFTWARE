    <p>
      <font size="4">Estudiar bertrand mayer, diseño por contrato </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Diseño por contrato </font>
    </p>
    <p>
      <font color="#000000" size="4">La robustez, la capasidad del software de reaccionar a casos no incluidos en la especificacion, de los posibles errores excepcionales se cubre generalmente con excepciones. </font>
    </p>
    <ul>
      <li>
        <font size="4">Por ejemplo. abrir un fichero&nbsp;&nbsp;no existente o sobre un soporte dañado, envio y recepcion de datos sin conexion de red o con la base de datos , uso inadecuado de una biblioteca, ...</font>
      </li>
    </ul>
    <p>
      <font size="4">La Correccion , la capasidad del software de ejecutar&nbsp;&nbsp;de acuerdo con sus especificaciones, de los posibles errores logicis se cubre generalmente inadecuadamente con Programacion Defensiva y&nbsp;&nbsp;si adecuadamente con Aserciones. </font>
    </p>
    <ul>
      <li>
        <font size="4">Por ejemplo . calculo de factorial de un numero negativi, bucle infinito, etc</font>
      </li>
    </ul>
    <p>
      
    </p>
    <p>
      <font size="4">Errores </font>
    </p>
    <p>
      <font size="4">Cuando queremos evitar un error &quot;como por ejemplo la divicion entre cero&quot; usamos un if para sacar por pantalla que ese es un error, pero&nbsp;&nbsp;eso esta mal. Si&nbsp;&nbsp;nosotros no tenemos acceso a la termina o usamos otra plataforma para el calculo jamas nos enterariamos de que no se realiso la divicion y que el programa ya ha terminado. </font>
    </p>
    <p>
      <font size="4">Una alternativa es el uso de exepciones, lo cual tambien esta mal. Las exepciones son para errores excepcionales, que se salen de los limites del software. Para las cosas de fuera </font>
    </p>
    <p>
      <font color="#ff0000" size="4">Ejm:</font><font size="4">&nbsp;La base de datos no se conecta, no se puede abrir el archivo, etc. </font>
    </p>
    <p>
      <font size="4">gestion de errores? </font>
    </p>
    <p>
      <font size="4">faltan excepciones? </font>
    </p>
    <p>
      <font size="4">Una Invariate de clase es una </font><font color="#008000" size="4">asercion</font><font size="4">&nbsp;expresada como una </font><font color="#008000" size="4">restriccion</font><font size="4">&nbsp;general de la consistencia a aplicar a cada objeto de la clase como un todo. </font>
    </p>
    <p>
      <font size="4">&nbsp;* Es diferente de las precondiciones y postcondiciones caracterizadas a rutinas individuales sobre sus parametros de entrada y sus resultados respectivaente junto con el estado del objeto. La invariante solo involucra el estado del objeto. </font>
    </p>
    <p>
      <font size="4">*Añadir Invaraintes de Clase fortalece o mantiene como poco las precondicones y postcondiciones porque la ivariante facilita el trabajo del componente que ademas de la pre condicion se puede asumir que el estado inicial del objeto&nbsp;&nbsp;cumple la invarioante , lo que restringe el conjunto de casos que deben contemplar. </font>
    </p>
    <p>
      <font size="4">** Complica el trabajo del comopnente porque ademas de la postcondicon, se deben cumplir queel estado final del objeto cumpla la invariante, lo que puede aumentar las acciones a&nbsp;&nbsp;?? </font>
    </p>
    <p>
      <font size="4">Bertrand Mayer llama a las precondicones como contratos </font>
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">Si el lenguage no admite aserts se debe crear un chanculoo paresido a </font>
    </p>
    <p>
      <font size="4">if(a == null) System.exit </font>
    </p>
    <p>
      <font size="4">Mas que errores este es el control de descuidos. </font>
    </p>
    <p>
      <font size="4">para los errores esta el testing </font>
    </p>
    <p>
      
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">La postcondicioens estan al servicio con las restricciones sobre elestado del valor devuelto y edel objeto servidor que se deben cumplir tras el ratorno de la opearcion para que el cliente progrese adecuadamente. Son luna oblicacion para el servidor y un beneficio para el cliente. </font>
    </p>
    <p>
      <font size="4">* Postcondiciones fuertes exigen ams al servidor que deben cumplir dica condicion y facilitan al cliete con un resultado mas restringido. </font>
    </p>
    <p>
      <font size="4">Postcondicones debiles exigen menos al servidor que debe de cumplir dica condicon y complican al cliente con un resulatdo mas abierot. </font>
    </p>
