      </li>
      <li>
        <font color="#000000" size="4">Estas aserciones se pueden usar: </font>

        <ul>
          <li>
            <font color="#000000" size="4">Eproducion, paradocumentar formalmetne (compilablemente) limites del ambito del componente sin efecto sobre al ejecucion . </font>
          </li>
          <li>
            <font color="#000000" size="4">En pre produccion , la comprobaciones automaticas durante la ejecucion y , en casao de error, elevar una excepcon que termina la ejecucion e informa claramente de lo que sucedio.</font>
