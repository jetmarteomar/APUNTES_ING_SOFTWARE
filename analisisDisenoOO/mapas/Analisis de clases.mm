<node TEXT="Analisis categorizacion clasica" ID="ID_1728966579" CREATED="1597292307653" MODIFIED="1653638442096" HGAP_QUANTITY="36.5 pt" VSHIFT_QUANTITY="-0.75 pt"><richcontent TYPE="NOTE" CONTENT-TYPE="xml/">
