  <head>
    
  </head>
  <body>
    <p>
      <b><font color="#993300" size="4"><u>El enfoque clasico o categorizacion clasica es darte una lista generica de entidades que cuando veas en tu proyecto, veas cuales son buenos candidatos para ser clases. </u></font></b>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#008000" size="4">Nota: El peligro de hacer lo primero que se te ocurre es pasar por alto muchas clases y por falta de esas clases vas a engrosar las clases que tienes</font></b>
    </p>
    <p>
      
    </p>
    <p>
      <b><font color="#ff0000" size="4">Estrategia</font><font size="4">: Buscar en el dominio del problema.</font></b>
    </p>
    <p>
      <b><font size="4">U</font></b><font size="4">n número de metodólogos han propuesto diversas fuentes de clases y objetos, derivados de los requisitos del <b>dominio del problema: </b></font>
    </p>
    <ul>
      <li>
        <u><font size="4">Cosas, objetos físicos o grupos de objetos que son tangibles:</font></u><font size="4">&nbsp;coches, datos de telemetría, sensores de presión </font>
      </li>
      <li>
        <u><font size="4">Conceptos, principios o ideas no tangibles </font></u><font size="4">por se utilizados para organizar o realizar un seguimiento de las actividades comerciales y/o comunicaciones: préstamo, reunión, intersección </font>
      </li>
      <li>
        <u><font size="4">Cosas que pasan, </font></u><font size="4">por lo general de otra cosa en una fecha determinada, eventos: aterrizaje, interrumpir, solicitud </font>
      </li>
      <li>
        <u><font size="4">Gente, seres humanos </font></u><font size="4">que llevan a cabo alguna función, usuarios que juegan diferentes roles en la interacción con la aplicación: madre, profesor, político </font>
      </li>
    </ul>
    <p>
      <b><font color="#ff0000" size="4">Encontrar fuentes de clases y objetos </font><font size="4">:</font></b>
    </p>
    <ul>
      <li>
        <u><font size="4">Organizaciones, </font></u><font size="4">colecciones formalmente organizadas de personas y recursos que tienen una misión definida, cuya existencia es en gran medida independiente de los individuos </font>
      </li>
      <li>
        <u><font size="4">Lugares físicos, </font></u><font size="4">oficinas y sitios importantes para la aplicación: zonas reservadas para personas o cosas </font>
      </li>
      <li>
        <u><font size="4">Dispositivos </font></u><font size="4">con los que interactúa la aplicación </font>
      </li>
      <li>
        <u><font size="4">Otros sistemas de sistemas externos </font></u><font size="4">con los que interactúa la aplicación </font>
      </li>
      <li>
        <font size="4"><u>Los eventos.</u>&nbsp;Claro que son clases. </font>
      </li>
    </ul>
    <p>
      
    </p>
    <p>
      <font size="4">Sustantivos son los ideales para nombres de clases. Los sustantivos definen al objeto. Los nombres de las clases siempre deben ser cosas.</font>
    </p>
    <p>
      <font size="4">Verbos para funciones. Definen la accion . Los nombres de los metodos siempre deben ser acciones.</font>
    </p>
    <p>
      
    </p>
    <p>
      <font size="4">Una buena estrategia es darte una lista de cosas genericas que cuando veas en tu ejemplo son buenos ejemplos para ser tus clases. </font>
    </p>
    <p>
      <font size="4">Ejemplo: </font>
    </p>
    <ul>
      <li>
        <font size="4">No tangible: un contrato es un concepto y es intangible pero es un objeto intangible.</font>
      </li>
      <li>
        <font size="4">Cosas que pasan (eventos) es algo que sucede. Cuando convenga se convierten los metodos en clases (por ejemplo los eventos en el swing de java). </font>
      </li>
      <li>
        <font size="4">Cuidado con cosificar todo, solo cuando sea necesario.(solo bajo ciertas sirscunstancias).</font>
      </li>
    </ul>
